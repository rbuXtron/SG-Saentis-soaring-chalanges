/**
 * Hilfsfunktion: Icon für Tab abrufen
 */
export function getTabIcon(tabId) {
  const iconName = ICON_MAPPINGS.tabs[tabId];
  return iconName ? SVG_ICONS[iconName] : null;
}

/**
 * Hilfsfunktion: Icon für Statistik-Karte abrufen
 */
export function getStatIcon(statId) {
  const iconName = ICON_MAPPINGS.stats[statId];
  return iconName ? SVG_ICONS[iconName] : null;
}

/**
 * Hilfsfunktion: Icon für Tabellen-Header abrufen
 */
export function getTableHeaderIcon(headerText) {
  const iconName = Object.entries(ICON_MAPPINGS.tableHeaders)
    .find(([text]) => headerText.includes(text))?.[1];
  return iconName ? TABLE_HEADER_SVG_ICONS[iconName] : null;
}